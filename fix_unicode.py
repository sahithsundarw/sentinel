"""Strip non-cp1252 chars from hackathon_simulation_test.py so it runs on Windows console."""
import re

src = open("hackathon_simulation_test.py", encoding="utf-8").read()

# Replace known symbols
replacements = [
    ("\u2550", "="), ("\u2554", "+"), ("\u2557", "+"), ("\u255a", "+"), ("\u255d", "+"),
    ("\u2588", "#"), ("\u2591", "."), ("\u2592", ":"), ("\u2593", "#"),
    ("\u2714", "+"), ("\u2718", "x"), ("\u26a0", "!"), ("\u2713", "+"), ("\u2715", "x"),
    ("\u2500", "-"), ("\u2502", "|"), ("\u2192", "->"), ("\u2022", "*"),
    ("\u2026", "..."), ("\u2018", "'"), ("\u2019", "'"), ("\u201c", '"'), ("\u201d", '"'),
    ("\u2013", "-"), ("\u2014", "--"), ("\u00bb", ">>"), ("\u00ab", "<<"),
    ("\u274c", "X"), ("\u2705", "OK"), ("\u2757", "!"), ("\u2753", "?"),
    ("\u25b6", ">"), ("\u25c4", "<"), ("\u25a0", "#"), ("\u25cf", "*"),
    ("\U0001f680", "[ROCKET]"), ("\U0001f4e6", "[PKG]"), ("\U0001f527", "[TOOL]"),
    ("\U0001f4ca", "[CHART]"), ("\U0001f4dd", "[NOTE]"), ("\U0001f512", "[LOCK]"),
    ("\U0001f6a8", "[ALERT]"), ("\U0001f4a1", "[IDEA]"), ("\U0001f9ea", "[TEST]"),
    ("\U0001f7e2", "[GREEN]"), ("\U0001f7e1", "[YELLOW]"), ("\U0001f534", "[RED]"),
    ("\U0001f916", "[BOT]"), ("\u2b50", "*"), ("\u2b55", "O"),
    ("\u2756", "*"), ("\u2762", "!"), ("\u2763", "!"),
    ("\u00d7", "x"), ("\u00b7", "."), ("\u00b2", "^2"), ("\u00b3", "^3"),
    ("\u00ae", "(R)"), ("\u00a9", "(C)"), ("\u2122", "(TM)"),
    ("\u221a", "+"), ("\u2248", "~="), ("\u2260", "!="), ("\u2264", "<="), ("\u2265", ">="),
    ("\u00e9", "e"), ("\u00e0", "a"), ("\u00e8", "e"), ("\u00f1", "n"),
    ("\u203c", "!!"), ("\u2049", "!?"),
    ("\u00b1", "+/-"),
    ("\u2776", "1"), ("\u2777", "2"), ("\u2778", "3"), ("\u2779", "4"), ("\u277a", "5"),
    ("\u2460", "1"), ("\u2461", "2"), ("\u2462", "3"), ("\u2463", "4"), ("\u2464", "5"),
    ("\u2744", "*"), ("\u2746", "*"),
    ("\u00f7", "/"), ("\u00b0", "deg"),
]
for old, new in replacements:
    src = src.replace(old, new)

# Replace any remaining non-ascii with '?'
safe = src.encode("ascii", errors="replace").decode("ascii")
open("hackathon_simulation_test.py", "w", encoding="utf-8").write(safe)
print("Done - unicode cleaned.")
